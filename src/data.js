/**
 * @type {({
 *      searchValue: string,
 *      isKatakana: boolean
 * }) => string}
 */
export const getKana = ({ searchValue = "", isKatakana = false }) => {
    searchValue = searchValue.trim();
    if (!searchValue) return;

    const { hiragana, katakana } = kana.find(
        ({ hiragana, katakana, romanji }) => Boolean(
            Boolean(
                isKatakana
                    ? katakana === searchValue
                    : hiragana === searchValue
            ) | romanji === searchValue
        )
    );

    return isKatakana ? katakana : hiragana;
}

export const getRandomKana = () => {
    const randomNumber = Math.random() * (kana.length - 1);
    const randomIndex = Math.round(randomNumber);

    return kana[randomIndex];
};